'''
Variáveis e tipos de dados:
Em python, você não precisa declarar o tipo de uma variável antes de usá-la. 
O tipo é inferido automaticamente pelo interpretador.
Em python a variável é declarada por padrão snake_case.
1. string -> palavras
2. number - int (número inteiro) ->
3. number - float (número decimal) ->
4. boolean - True ou False (Verdadeiro ou Falso) ->
'''

