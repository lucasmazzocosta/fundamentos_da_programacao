'''
Operadores matemáticos
'''

numero_um = 20
numero_dois = 15

soma = numero_um + numero_dois
subtracao = numero_um - numero_dois
multiplicacao = numero_um * numero_dois
divisao = numero_um / numero_dois
exponenciacao = numero_um ** numero_dois
modulo = numero_um % numero_dois

print(f'A soma eh de {soma}')
print(f'A subtracao eh de {subtracao}')
print(f'A multiplicacao eh de {multiplicacao}')
print(f'A divisao eh de {divisao:.2f}')
print(f'A exponenciacao eh de {exponenciacao:2e}')
print(f'O modulo eh de {modulo}')